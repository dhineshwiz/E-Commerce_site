package in.ec.param;

public class ListProductsParam {

	String Fashion;
	String Electronics;
	String Books;
	String Utensils;

	public String getFashion() {
		return Fashion;
	}

	public void setFashion(String fashion) {
		Fashion = fashion;
	}

	public String getElectronics() {
		return Electronics;
	}

	public void setElectronics(String electronics) {
		Electronics = electronics;
	}

	public String getBooks() {
		return Books;
	}

	public void setBooks(String books) {
		Books = books;
	}

	public String getUtensils() {
		return Utensils;
	}

	public void setUtensils(String utensils) {
		Utensils = utensils;
	}

}
